void main(List<String> args) {
  //Comparision (==,!=,>,>=,<,<=)

  bool equal = 1 == 1;
  bool notEqual = "Best" != "Dev";
  bool greaterThan = 10 > 9;
  bool greaterThanOrEqual = 10 >= 9;
  bool lessThan = 10 < 9;
  bool lessThanOrEqual = 10 <= 9;

  print("1 equal 1 = ${equal}");
  print(' "Best" not equal "Dev" = ${notEqual}');
  print('10 greater than 9 = ${greaterThan}');
  print('10 greater than or equal 9 =${greaterThanOrEqual}');
  print('10 lessthan 9 = ${lessThan}');
  print('10 lessthan or equal 9 = ${lessThanOrEqual}');
}
